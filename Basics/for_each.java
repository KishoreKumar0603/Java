
public class For_each {

    public static void main(String[] args){
        int Number[] = {1,3,4,5,6,8};
        for(int i : Number){
            System.out.println(i);
        }

        String str[] = {"kishore","jana","durai"};
        for(String i : str){
            System.out.println(i);
        }
    }
}